const mmds: Record<number, ArrayBuffer> = {};

export const MMDS = (index: number) => {
  if (mmds[index]) {
    return mmds[index];
  }

  const { pointer, byteLength } = MMD_POINTERS[index] || {};
  if (!pointer || !byteLength) {
    throw Error("Digimon does not exist");
  }

  mmds[index] = window.value.slice(pointer, pointer + byteLength + 1);

  return mmds[index];
};
